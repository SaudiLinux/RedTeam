def scan(url):
    headers = {"User-Agent": random.choice(UA)}
    r = requests.get(url, headers=headers, verify=False)
    print("Status:", r.status_code)

if __name__ == "__main__":
    banner()
    if len(sys.argv) != 2:
        print("Usage: python redteam_stealth.py https://target.com")
        sys.exit(1)
    scan(sys.argv[1])
