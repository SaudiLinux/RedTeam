def fingerprint(url):
    r = requests.get(url, verify=False)
    headers = r.headers

    if "cf-ray" in headers:
        print("[+] Cloudflare detected")
    elif "x-amzn-requestid" in headers:
        print("[+] AWS WAF detected")
    else:
        print("[*] Unknown WAF")

if __name__ == "__main__":
    banner()
    if len(sys.argv) != 2:
        print("Usage: python redteam_waf_advanced.py https://target.com")
        sys.exit(1)
    fingerprint(sys.argv[1])
